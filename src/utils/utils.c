#include "../../includes/cub3d.h"

void	exit_error(char *err)
{
	int	i;

	write(2, "Error\n", 6);
	i = 0;
	while (err[i])
	{
		write(2, &err[i], 1);
		i++;
	}
	write(2, "\n", 1);
	exit(1);
}
